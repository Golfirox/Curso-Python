#Calcular el perímetro y área de un rectángulo dada su base y su altura. Calcular también el  perímetro y área de un triangulo.  
print ("Vamos a calcular el area y el perimetro de un rectangulo ")
l=int (input ("Dime cuanto mide un lado "))
a=int (input ("Dime su altura "))
A=(l*a)
P=(2*l)+(2*a)
print ("El area del rectangulo es de ", A)
print ("El perimetro del rectangulo es de ", P) 